-- E. Trigger for Profile Updates
create or replace function public.log_profile_update_activity()
returns trigger as $$
begin
  -- Trigger only on display name or username changes
  if (old.full_name is distinct from new.full_name or old.username is distinct from new.username) then
    insert into public.activities (actor_id, action_type, description, metadata)
    values (
      new.id,
      'profile.update',
      coalesce(new.full_name, new.username, 'A user') || ' updated their profile details.',
      jsonb_build_object('full_name', new.full_name, 'username', new.username)
    );
  end if;
  return new;
end;
$$ language plpgsql security definer;

create or replace trigger tr_profile_update_activity
  after update on public.profiles
  for each row execute procedure public.log_profile_update_activity();

-- Database Schema Migration: Realtime Activity Feed
-- =======================================================

-- 1. Create activities table
create table if not exists public.activities (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  actor_id uuid references public.profiles(id) on delete cascade not null,
  action_type text not null, -- 'post.create', 'post.like', 'comment.create', 'user.follow', 'profile.update'
  target_id text,
  description text not null,
  metadata jsonb default '{}'::jsonb not null
);

-- Enable Row Level Security (RLS)
alter table public.activities enable row level security;

-- RLS Policies for activities
-- Allow public select access so anyone on the platform can see the activity timeline
create policy "Allow public read access to activities" on public.activities
  for select using (true);

-- Allow authenticated users to write activities directly if necessary
create policy "Allow authenticated users to insert activities" on public.activities
  for insert with check (auth.uid() = actor_id);

-- Configure Replica Identity to FULL for deletes
alter table public.activities replica identity full;

-- Enable Realtime Broadcasting
alter publication supabase_realtime add table public.activities;


-- =======================================================
-- Trigger Functions for Automatic Activity Tracking
-- =======================================================

-- A. Trigger for Post Creation
create or replace function public.log_post_creation_activity()
returns trigger as $$
declare
  v_full_name text;
begin
  select full_name into v_full_name from public.profiles where id = new.user_id;
  
  insert into public.activities (actor_id, action_type, target_id, description, metadata)
  values (
    new.user_id,
    'post.create',
    new.id::text,
    coalesce(v_full_name, 'A user') || ' published a new post: "' || substring(new.title from 1 for 50) || '"',
    jsonb_build_object('post_id', new.id, 'title', new.title)
  );
  return new;
end;
$$ language plpgsql security definer;

create or replace trigger tr_post_creation_activity
  after insert on public.posts
  for each row execute procedure public.log_post_creation_activity();



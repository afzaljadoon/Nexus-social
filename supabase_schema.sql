-- 10. Database function and trigger for automatic user initialization after signup
create or replace function public.handle_new_user()
returns trigger as $$
begin
  -- 1. Create public profile
  insert into public.profiles (id, full_name, username, avatar_url)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'full_name', split_part(new.email, '@', 1)),
    coalesce(new.raw_user_meta_data->>'username', split_part(new.email, '@', 1)),
    coalesce(new.raw_user_meta_data->>'avatar_url', null)
  );

  -- 2. Create user settings row
  insert into public.user_settings (user_id)
  values (new.id);

  -- 3. Create notification preferences row
  insert into public.notification_preferences (user_id)
  values (new.id);

  return new;
end;
$$ language plpgsql security definer;

-- Trigger to execute function after a row is inserted in auth.users
create or replace trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

-- 11. Role security triggers and moderation policies
alter table public.profiles add column if not exists role text default 'user' not null;
alter table public.profiles add column if not exists is_banned boolean default false not null;

-- Trigger function to prevent standard users from upgrading their own role or unbanning themselves
create or replace function public.check_role_modification()
returns trigger as $$
begin
  if (old.role <> new.role or old.is_banned <> new.is_banned) then
    if auth.uid() is not null and not exists (
      select 1 from public.profiles
      where id = auth.uid() and role = 'admin'
    ) then
      raise exception 'Unauthorized: Only administrators can modify user roles or ban status.';
    end if;
  end if;
  return new;
end;
$$ language plpgsql security definer;

create or replace trigger trigger_check_role_modification
  before update on public.profiles
  for each row execute procedure public.check_role_modification();

-- Policies to allow administrators and moderators to delete any post or comment
create policy "Allow admins and moderators to delete any post" on public.posts
  for delete using (
    exists (
      select 1 from public.profiles
      where id = auth.uid() and role in ('admin', 'moderator')
    )
  );

create policy "Allow admins and moderators to delete any comment" on public.comments
  for delete using (
    exists (
      select 1 from public.profiles
      where id = auth.uid() and role in ('admin', 'moderator')
    )
  );

-- =======================================================
-- Database Schema Migration: Comments & Likes Tables
-- =======================================================

-- 1. Create comments table
create table if not exists public.comments (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone,
  content text not null,
  post_id bigint references public.posts(id) on delete cascade not null,
  user_id uuid references public.profiles(id) on delete cascade not null
);

-- Enable Row Level Security (RLS) for comments
alter table public.comments enable row level security;

-- RLS Policies for comments
create policy "Allow public read access to comments" on public.comments
  for select using (true);

create policy "Allow authenticated users to insert comments" on public.comments
  for insert with check (auth.uid() = user_id);

create policy "Allow users to update their own comments" on public.comments
  for update using (auth.uid() = user_id);

create policy "Allow users to delete their own comments" on public.comments
  for delete using (auth.uid() = user_id);


-- 2. Create likes table (Many-to-Many Relationship)
create table if not exists public.likes (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  post_id bigint references public.posts(id) on delete cascade not null,
  user_id uuid references public.profiles(id) on delete cascade not null,
  
  -- Duplicate Prevention: Ensure a profile can only like a post once
  constraint unique_post_user_like unique (post_id, user_id)
);

-- Enable Row Level Security (RLS) for likes
alter table public.likes enable row level security;

-- RLS Policies for likes
create policy "Allow public read access to likes" on public.likes
  for select using (true);

create policy "Allow authenticated users to insert likes" on public.likes
  for insert with check (auth.uid() = user_id);

create policy "Allow users to delete their own likes" on public.likes
  for delete using (auth.uid() = user_id);

-- Configure Replica Identity to FULL so that DELETE realtime payloads contain user_id and post_id
alter table public.likes replica identity full;

-- 3. Enable Realtime Broadcasting
alter publication supabase_realtime add table public.likes;
alter publication supabase_realtime add table public.comments;


-- 4. Create bookmarks table (Relationship Table)
create table if not exists public.bookmarks (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  post_id bigint references public.posts(id) on delete cascade not null,
  user_id uuid references public.profiles(id) on delete cascade not null,
  
  -- Duplicate Prevention: Ensure a user can only bookmark a post once
  constraint unique_post_user_bookmark unique (post_id, user_id)
);

-- Enable Row Level Security (RLS) for bookmarks
alter table public.bookmarks enable row level security;

-- RLS Policies for bookmarks (Private: Only the bookmark owner can read their own bookmarks)
create policy "Allow users to view their own bookmarks" on public.bookmarks
  for select using (auth.uid() = user_id);

create policy "Allow authenticated users to insert bookmarks" on public.bookmarks
  for insert with check (auth.uid() = user_id);

create policy "Allow users to delete their own bookmarks" on public.bookmarks
  for delete using (auth.uid() = user_id);

-- Configure Replica Identity to FULL so that DELETE realtime payloads contain user_id and post_id
alter table public.bookmarks replica identity full;

-- Enable Realtime Broadcasting for Bookmarks
alter publication supabase_realtime add table public.bookmarks;


-- 5. Create notifications table
create table if not exists public.notifications (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  recipient_id uuid references public.profiles(id) on delete cascade not null,
  sender_id uuid references public.profiles(id) on delete cascade not null,
  type text not null, -- 'like', 'comment', or 'follow'
  post_id bigint references public.posts(id) on delete cascade,
  is_read boolean default false not null
);

-- Enable Row Level Security (RLS) for notifications
alter table public.notifications enable row level security;

-- RLS Policies for notifications (Private: Only the recipient can read and update their own notifications)
create policy "Allow users to view their own notifications" on public.notifications
  for select using (auth.uid() = recipient_id);

create policy "Allow authenticated users to insert notifications" on public.notifications
  for insert with check (auth.uid() = sender_id);

create policy "Allow users to update their own notifications" on public.notifications
  for update using (auth.uid() = recipient_id);

-- Configure Replica Identity to FULL
alter table public.notifications replica identity full;

-- Enable Realtime Broadcasting for Notifications
alter publication supabase_realtime add table public.notifications;


-- 6. Create follows table (Self-referencing Many-to-Many Relationship)
create table if not exists public.follows (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  follower_id uuid references public.profiles(id) on delete cascade not null,
  following_id uuid references public.profiles(id) on delete cascade not null,
  
  -- Prevent users from following themselves
  constraint check_not_self_follow check (follower_id <> following_id),
  -- Prevent duplicate follow entries
  constraint unique_follower_following unique (follower_id, following_id)
);

-- Enable Row Level Security (RLS) for follows
alter table public.follows enable row level security;

-- RLS Policies for follows
create policy "Allow public read access to follows" on public.follows
  for select using (true);

create policy "Allow authenticated users to insert follows" on public.follows
  for insert with check (auth.uid() = follower_id);

create policy "Allow users to delete their own follows" on public.follows
  for delete using (auth.uid() = follower_id);

-- Configure Replica Identity to FULL
alter table public.follows replica identity full;

-- Enable Realtime Broadcasting for Follows
alter publication supabase_realtime add table public.follows;

-- 7. Indexes for Query Optimization

-- Database Schema Migration: Event-Driven Architecture (EDA)
-- =======================================================

-- 1. Create event_store table (Transaction Ledger)
create table if not exists public.event_store (
  id uuid primary key default gen_random_uuid(),
  event_type text not null, -- e.g. 'post.created', 'comment.created', 'post.liked', 'user.followed'
  payload jsonb default '{}'::jsonb not null,
  actor_id uuid references public.profiles(id) on delete set null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable RLS for Event Store (Read-only for Admins/Audit, system writes)
alter table public.event_store enable row level security;

drop policy if exists "Allow admins and audit roles to select event_store" on public.event_store;
create policy "Allow admins and audit roles to select event_store" on public.event_store
  for select using (
    exists (
      select 1 from public.profiles
      where id = auth.uid() and role = 'admin'
    )
  );

drop policy if exists "Allow authenticated users to insert event_store" on public.event_store;
create policy "Allow authenticated users to insert event_store" on public.event_store
  for insert with check (true);

-- 2. Create event_queue table (Transactional Outbox Queue)
create table if not exists public.event_queue (
  id bigint generated by default as identity primary key,
  event_id uuid references public.event_store(id) on delete cascade not null,
  status text default 'pending' not null check (status in ('pending', 'processing', 'completed', 'failed')),
  attempts integer default 0 not null,
  max_attempts integer default 5 not null,
  error_message text,
  process_after timestamp with time zone default timezone('utc'::text, now()) not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable RLS for Event Queue (Read-only for Admins)
alter table public.event_queue enable row level security;

drop policy if exists "Allow admins to select event_queue" on public.event_queue;
create policy "Allow admins to select event_queue" on public.event_queue
  for select using (
    exists (
      select 1 from public.profiles
      where id = auth.uid() and role = 'admin'
    )
  );

drop policy if exists "Allow authenticated users to insert event_queue" on public.event_queue;
create policy "Allow authenticated users to insert event_queue" on public.event_queue
  for insert with check (true);

-- 3. Trigger to automatically enqueue new events from the event store
create or replace function public.enqueue_new_event()
returns trigger as $$
begin
  insert into public.event_queue (event_id)
  values (new.id);
  return new;
end;
$$ language plpgsql security definer;

create or replace trigger trigger_enqueue_new_event
  after insert on public.event_store
  for each row execute procedure public.enqueue_new_event();

-- 4. Central event publishing helper function
create or replace function public.publish_event(
  p_event_type text,
  p_payload jsonb,
  p_actor_id uuid
)
returns uuid as $$
declare
  v_event_id uuid;
begin
  insert into public.event_store (event_type, payload, actor_id)
  values (p_event_type, p_payload, p_actor_id)
  returning id into v_event_id;
  
  return v_event_id;
end;
$$ language plpgsql security definer;

-- 5. Set up triggers on operational tables to automatically publish events
-- A. Posts Trigger
create or replace function public.publish_post_event()
returns trigger as $$
begin
  if tg_op = 'INSERT' then
    perform public.publish_event(
      'post.created',
      jsonb_build_object(
        'id', new.id,
        'title', new.title,
        'user_id', new.user_id,
        'created_at', new.created_at
      ),
      new.user_id
    );
  end if;
  return new;
end;
$$ language plpgsql security definer;


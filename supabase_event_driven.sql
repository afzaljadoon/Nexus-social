-- Asynchronous handlers (executed by outbox consumer)
create or replace function public.handle_async_event(
  p_event_type text,
  p_payload jsonb,
  p_actor_id uuid
)
returns void as $$
declare
  v_full_name text;
  v_post_title text;
  v_post_author_id uuid;
  v_following_name text;
begin
  -- 1. Asynchronous Gamification & Activity Feed logic based on Event Type
  if p_event_type = 'post.created' then
    -- A. Update reputation +10
    update public.profiles set reputation_points = reputation_points + 10 where id = p_actor_id;
    perform public.check_user_badges(p_actor_id);
    
    -- B. Add activity log
    select full_name into v_full_name from public.profiles where id = p_actor_id;
    insert into public.activities (actor_id, action_type, target_id, description, metadata)
    values (
      p_actor_id,
      'post.create',
      p_payload->>'id',
      coalesce(v_full_name, 'A user') || ' published a new post: "' || substring(p_payload->>'title' from 1 for 50) || '"',
      jsonb_build_object('post_id', p_payload->>'id', 'title', p_payload->>'title')
    );

  elsif p_event_type = 'comment.created' then
    -- A. Update reputation +5
    update public.profiles set reputation_points = reputation_points + 5 where id = p_actor_id;
    perform public.check_user_badges(p_actor_id);

    -- B. Add activity log
    select full_name into v_full_name from public.profiles where id = p_actor_id;
    select title into v_post_title from public.posts where id = (p_payload->>'post_id')::bigint;
    insert into public.activities (actor_id, action_type, target_id, description, metadata)
    values (
      p_actor_id,
      'comment.create',
      p_payload->>'post_id',
      coalesce(v_full_name, 'A user') || ' commented on "' || coalesce(v_post_title, 'a post') || '": "' || substring(p_payload->>'content' from 1 for 40) || '..."',
      jsonb_build_object('post_id', p_payload->>'post_id', 'comment_id', p_payload->>'id', 'post_title', v_post_title)
    );

  elsif p_event_type = 'post.liked' then
    -- A. Update reputation +5 for post author
    select user_id into v_post_author_id from public.posts where id = (p_payload->>'post_id')::bigint;
    if v_post_author_id is not null then
      update public.profiles set reputation_points = reputation_points + 5 where id = v_post_author_id;
      perform public.check_user_badges(v_post_author_id);
    end if;

    -- B. Add activity log
    select full_name into v_full_name from public.profiles where id = p_actor_id;
    select title into v_post_title from public.posts where id = (p_payload->>'post_id')::bigint;
    insert into public.activities (actor_id, action_type, target_id, description, metadata)
    values (
      p_actor_id,
      'post.like',
      p_payload->>'post_id',
      coalesce(v_full_name, 'A user') || ' liked the post: "' || coalesce(v_post_title, 'Untitled') || '"',
      jsonb_build_object('post_id', p_payload->>'post_id', 'post_title', v_post_title)
    );

  elsif p_event_type = 'user.followed' then
    -- A. Update reputation +2 for follower
    update public.profiles set reputation_points = reputation_points + 2 where id = p_actor_id;
    perform public.check_user_badges(p_actor_id);

    -- B. Add activity log
    select full_name into v_full_name from public.profiles where id = p_actor_id;
    select full_name into v_following_name from public.profiles where id = (p_payload->>'following_id')::uuid;
    insert into public.activities (actor_id, action_type, target_id, description, metadata)
    values (
      p_actor_id,
      'user.follow',
      p_payload->>'following_id',
      coalesce(v_full_name, 'A user') || ' started following ' || coalesce(v_following_name, 'another user'),
      jsonb_build_object('following_id', p_payload->>'following_id', 'following_name', v_following_name)
    );
  end if;
end;
$$ language plpgsql security definer;

-- 6. Central Asynchronous Queue Consumer Function
-- Grabs pending items, marks them, routes them, and updates their final status.
create or replace function public.process_event_queue(p_batch_size integer default 10)
returns table (
  processed_count integer,
  failed_count integer
) as $$
declare
  v_item record;
  v_processed integer := 0;
  v_failed integer := 0;
begin
  -- Loop through pending event queue items
  for v_item in 
    select q.id, q.attempts, s.event_type, s.payload, s.actor_id 
    from public.event_queue q
    join public.event_store s on q.event_id = s.id
    where q.status in ('pending', 'failed')
      and q.attempts < q.max_attempts
      and q.process_after <= timezone('utc'::text, now())
    order by q.created_at asc
    limit p_batch_size
    for update skip locked -- Prevent concurrent workers from picking the same items (optimistic locking)
  loop
    begin
      -- Mark item as processing
      update public.event_queue
      set status = 'processing',
          attempts = attempts + 1,
          updated_at = timezone('utc'::text, now())
      where id = v_item.id;

      -- Execute handlers asynchronously
      perform public.handle_async_event(v_item.event_type, v_item.payload, v_item.actor_id);

      -- Mark completed
      update public.event_queue
      set status = 'completed',
          updated_at = timezone('utc'::text, now())
      where id = v_item.id;
      
      v_processed := v_processed + 1;

    exception when others then
      -- Log error and mark failed with backoff delay (attempts * 1 minute delay)
      update public.event_queue
      set status = 'failed',
          error_message = SQLERRM,
          process_after = timezone('utc'::text, now()) + (attempts * interval '1 minute'),
          updated_at = timezone('utc'::text, now())
      where id = v_item.id;
      
      v_failed := v_failed + 1;
    end;
  end loop;

  processed_count := v_processed;
  failed_count := v_failed;
  return next;
end;
$$ language plpgsql security definer;
